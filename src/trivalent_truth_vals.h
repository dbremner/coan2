/** \brief  `struct trivalent_truth_values` encapulates an
 *  enumeration of non-boolean truth values and a count of same
 */
struct truth_value {

    /// Enumeration of possible true values of `#if`s
    enum {
        /// Falsified by configuration
        False = 0,
        /// Neither falsified nor verified by configuration
        Indeterminate = 1,
        /// Verified by configurarion
        True = 2
    };
    /// The number of truth values
    static const unsigned n_values = 3;
};

